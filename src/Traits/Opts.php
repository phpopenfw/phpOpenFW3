namespace phpOpenFW\Traits;

//**************************************************************************************
/**
 * Class Options Trait
 */
//**************************************************************************************
trait Opts
{
    //=========================================================================
	// Trait Memebers
    //=========================================================================
	protected $opts = [];

    //=========================================================================
    //=========================================================================
    // Set Option Method
    //=========================================================================
    //=========================================================================
    public function SetOpt($key, $val)
    {
	    if ($key == '') { return false; }
	    $this->opts[$key] = $val;
	    return $this;
    }

    //=========================================================================
    //=========================================================================
    // Get Option Method
    //=========================================================================
    //=========================================================================
    public function GetOpt($key)
    {
	    if ($key == '') { return false; }
	    if (isset($this->opts[$key])) {
		    return $this->opts[$key];
	    }
	    return false;
    }
}
