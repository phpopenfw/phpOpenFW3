namespace phpOpenFW\Builders\SQL;

//**************************************************************************************
/**
 * SQL Select Class
 */
//**************************************************************************************
class Select extends Statement
{
    //=========================================================================
    // Traits
    //=========================================================================
    use Traits\Select;
    use Traits\From;
    use Traits\Join;
    use Traits\Where;
    use Traits\GroupBy;
    use Traits\OrderBy;
    use Traits\Having;
    use Traits\Limit;
    use Traits\Union;

    //=========================================================================
	// Class Memebers
    //=========================================================================
    protected $sql_type = 'select';

    //=========================================================================
    //=========================================================================
    // Constructor Method
    //=========================================================================
    //=========================================================================
    public function __construct($table, $data_source=false)
    {
        parent::__construct($table, $data_source);
        if ($this->table) {
            $this->from[] = $this->table;
        }
    }

    //=========================================================================
    //=========================================================================
    // Get Method
    //=========================================================================
    //=========================================================================
    public function GetSQL()
    {
		//-------------------------------------------------------
        // Set / Reset Bind Parameters
		//-------------------------------------------------------
        if ($this->parent_query) {
            $this->bind_params = $this->parent_query->GetBindParams();
        }
        else {
            $this->bind_params = [];
        }

		//-------------------------------------------------------
        // Start SQL
		//-------------------------------------------------------
        $strsql = '';

		//-------------------------------------------------------
		// Select Fields / From Tables...
		//-------------------------------------------------------
		self::AddSQLClause($strsql, $this->FormatFields());
		self::AddSQLClause($strsql, $this->FormatFrom());

		//-------------------------------------------------------
		// Where
		//-------------------------------------------------------
		self::AddSQLClause($strsql, $this->FormatWhere());

		//-------------------------------------------------------
		// Group By
		//-------------------------------------------------------
		self::AddSQLClause($strsql, $this->FormatGroupBy());

		//-------------------------------------------------------
		// Unions
		//-------------------------------------------------------
		self::AddSQLClause($strsql, $this->FormatUnions());

		//-------------------------------------------------------
		// Having
		//-------------------------------------------------------
		self::AddSQLClause($strsql, $this->FormatHaving());

		//-------------------------------------------------------
		// Order By
		//-------------------------------------------------------
		self::AddSQLClause($strsql, $this->FormatOrderBy());

		//-------------------------------------------------------
		// Limit
		//-------------------------------------------------------
		$limit = $this->FormatLimit();
		if ($limit) {
    		if ($this->db_type == 'sqlsrv') {
        		$strsql = str_replace('SELECT ', $limit . ' ', $strsql);
    		}
    		else {
        		$strsql .= $limit . "\n";
    		}
		}

		//-------------------------------------------------------
		// Return SQL
		//-------------------------------------------------------
		return $strsql;
	}

    //=========================================================================
    //=========================================================================
	// Add SQL Clause Method
    //=========================================================================
    //=========================================================================
	protected static function AddSQLClause(&$strsql, $clause)
	{
		if ($clause) {
    		$strsql .= $clause . "\n";
            return true;
        }

        return false;
    }

}
